"""Answer generation: OpenAI when available, extractive fallback otherwise."""

from __future__ import annotations

from dataclasses import dataclass

from src.chunking import Chunk
from src.config import OPENAI_API_KEY, OPENAI_MODEL


@dataclass
class GenerationResult:
    answer: str
    cited_chunk_ids: list[str]
    input_tokens: int
    output_tokens: int
    model_used: str        # "openai" | "extractive_fallback"


_SYSTEM_PROMPT = """\
You are a clinical information assistant.
Answer the question using ONLY the provided context chunks.
At the end of your answer, list the chunk IDs you drew from, formatted as:
  Sources: chunk_id_1, chunk_id_2

If the context does not contain enough information, say:
  "The provided guidelines do not cover this topic sufficiently."

Do NOT provide medical advice. This is a RAG evaluation demo only.
"""


def generate_answer(
    query: str,
    chunks: list[tuple[Chunk, float]],
) -> GenerationResult:
    """Route to OpenAI or extractive fallback based on API key availability."""
    if OPENAI_API_KEY:
        return _openai_generate(query, chunks)
    return _extractive_fallback(query, chunks)


# ── OpenAI path ───────────────────────────────────────────────────────────────

def _openai_generate(query: str, chunks: list[tuple[Chunk, float]]) -> GenerationResult:
    try:
        from openai import OpenAI
    except ImportError:
        raise ImportError("pip install openai")

    client = OpenAI(api_key=OPENAI_API_KEY)

    context = _format_context(chunks)
    messages = [
        {"role": "system", "content": _SYSTEM_PROMPT},
        {
            "role": "user",
            "content": f"Context:\n{context}\n\nQuestion: {query}",
        },
    ]

    response = client.chat.completions.create(
        model=OPENAI_MODEL,
        messages=messages,
        temperature=0.0,
        max_tokens=512,
    )

    answer = response.choices[0].message.content.strip()
    cited = _extract_cited_ids(answer, chunks)

    return GenerationResult(
        answer=answer,
        cited_chunk_ids=cited,
        input_tokens=response.usage.prompt_tokens,
        output_tokens=response.usage.completion_tokens,
        model_used="openai",
    )


# ── Extractive fallback ───────────────────────────────────────────────────────

def _extractive_fallback(query: str, chunks: list[tuple[Chunk, float]]) -> GenerationResult:
    """Select the highest-scoring chunk and return its most relevant sentences."""
    if not chunks:
        return GenerationResult(
            answer="No context chunks were retrieved.",
            cited_chunk_ids=[],
            input_tokens=0,
            output_tokens=0,
            model_used="extractive_fallback",
        )

    query_words = set(query.lower().split())
    best_chunk, best_score = chunks[0]

    # Score sentences by keyword overlap with the query
    sentences = [s.strip() for s in best_chunk.text.replace("\n", " ").split(".") if s.strip()]
    scored = []
    for sent in sentences:
        overlap = len(query_words & set(sent.lower().split()))
        scored.append((overlap, sent))
    scored.sort(reverse=True)

    top_sentences = [s for _, s in scored[:3]]
    answer_body = ". ".join(top_sentences) + "."
    cited_ids = [c.chunk_id for c, _ in chunks[:3]]

    answer = (
        f"{answer_body}\n\n"
        f"Sources: {', '.join(cited_ids)}\n\n"
        f"[Note: Generated by extractive fallback — no OpenAI key detected.]"
    )

    words = len(answer.split())
    return GenerationResult(
        answer=answer,
        cited_chunk_ids=cited_ids,
        input_tokens=sum(len(c.text.split()) for c, _ in chunks),
        output_tokens=words,
        model_used="extractive_fallback",
    )


# ── Helpers ───────────────────────────────────────────────────────────────────

def _format_context(chunks: list[tuple[Chunk, float]]) -> str:
    parts = []
    for chunk, score in chunks:
        parts.append(
            f"[{chunk.chunk_id}] (score={score:.3f})\n{chunk.text}"
        )
    return "\n\n---\n\n".join(parts)


def _extract_cited_ids(answer: str, chunks: list[tuple[Chunk, float]]) -> list[str]:
    """Find chunk IDs that appear in the answer text."""
    chunk_ids = [c.chunk_id for c, _ in chunks]
    return [cid for cid in chunk_ids if cid in answer]
